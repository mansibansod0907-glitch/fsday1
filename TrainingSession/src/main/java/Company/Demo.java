package Company;


public class Demo {

	public static void main(String[] args) {
		int a = 45;
		System.out.println(a);   //decimal
		
		int b = 045; 
		System.out.println(b); //octal
		
		int c= 0x45;
		System.out.println(c); //hexadecimal
		
		int d = 0b1011;
		System.out.println(d);  //binary
	
	}

}
