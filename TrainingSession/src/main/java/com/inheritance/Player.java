package com.inheritance;

public class Player {
 String name;
 int age;
 String gender;
 int bestscore;
 String country;
 String team;
 
 public Player(String name, int age, String gender, int bestscore,
         String country, String team) {
this.name = name;
this.age = age;
this.gender = gender;
this.bestscore = bestscore;
this.country = country;
this.team = team;
}
 }


	
	
	

